// ============================================================================
//  effect.js — сердце реактивности
// ----------------------------------------------------------------------------
//  Идея одной фразой: «эффект» — это функция, которую нужно перезапустить,
//  когда изменятся данные, которые она читала». Всё остальное в реактивности
//  строится вокруг двух глаголов:
//
//    track(target, key)   — «запомни, что текущий эффект прочитал target.key»
//    trigger(target, key) — «target.key изменился — перезапусти все эффекты,
//                            которые его читали»
//
//  Ниже мы храним эти связи в структуре targetMap и аккуратно ими управляем.
// ============================================================================

// Кто сейчас «слушает»? Пока выполняется тело какого-то эффекта, здесь лежит
// этот эффект. Любое чтение реактивных данных в этот момент попадёт в его
// список зависимостей. Вне эффектов activeEffect === undefined, и чтение
// данных ничего не отслеживает (просто вернёт значение).
export let activeEffect = undefined

// Эффекты могут быть вложенными (эффект внутри эффекта — например, компонент,
// внутри которого работает computed). Чтобы после выхода из внутреннего эффекта
// «активным» снова стал внешний, мы держим стек.
const effectStack = []

// Главное хранилище всех зависимостей приложения:
//
//   targetMap: WeakMap {
//     реактивныйОбъект -> depsMap: Map {
//       'имяСвойства' -> dep: Set(эффект1, эффект2, ...)
//     }
//   }
//
// WeakMap — чтобы объект, который больше нигде не используется, мог быть собран
// сборщиком мусора вместе со своими зависимостями (мы его не «держим»).
const targetMap = new WeakMap()

// ---------------------------------------------------------------------------
//  ReactiveEffect — эффект как объект, а не просто функция.
//  Оборачиваем функцию в класс, чтобы к ней можно было прицепить:
//    - deps      : список Set'ов, в которых этот эффект «прописан»
//                  (нужен, чтобы вычищать устаревшие зависимости);
//    - scheduler : необязательный «планировщик» — если он задан, при trigger
//                  вызовется он вместо немедленного run() (так работают
//                  computed и watch, а позже — асинхронное обновление UI).
// ---------------------------------------------------------------------------
export class ReactiveEffect {
  constructor(fn, scheduler = null) {
    this.fn = fn
    this.scheduler = scheduler
    this.deps = []
    this.active = true // false после stop() — такой эффект больше не отслеживает
  }

  run() {
    // Остановленный эффект просто выполняет функцию без отслеживания.
    if (!this.active) return this.fn()

    // Перед запуском вычищаем старые зависимости. Пример, зачем это нужно:
    //   () => text.value = ok.value ? a.value : b.value
    // Когда ok === true, эффект читает a, но НЕ b. Если не чистить, эффект
    // навсегда останется подписан и на b, и будет зря перезапускаться при
    // изменении b. Поэтому перед каждым запуском забываем всё и собираем
    // зависимости заново — так остаются только реально прочитанные в этот раз.
    cleanup(this)

    try {
      effectStack.push(this)
      activeEffect = this
      return this.fn()
    } finally {
      // Что бы ни случилось, восстанавливаем предыдущий активный эффект.
      effectStack.pop()
      activeEffect = effectStack[effectStack.length - 1]
    }
  }

  stop() {
    if (this.active) {
      cleanup(this)
      this.active = false
    }
  }
}

// Убираем эффект из всех Set'ов, где он записан, и очищаем его собственный
// список deps. После этого эффект «ни на что не подписан».
function cleanup(effect) {
  const { deps } = effect
  for (const dep of deps) {
    dep.delete(effect)
  }
  deps.length = 0
}

// ---------------------------------------------------------------------------
//  effect(fn) — публичная функция. Создаёт эффект и сразу запускает его,
//  чтобы он собрал свои зависимости. Возвращает «раннер» — функцию, которой
//  можно перезапустить эффект вручную, и у которой есть .effect для stop().
// ---------------------------------------------------------------------------
export function effect(fn, options = {}) {
  const _effect = new ReactiveEffect(fn, options.scheduler)

  // lazy: true — не запускать сразу (нужно для computed, который считается
  // только когда к нему обратятся).
  if (!options.lazy) {
    _effect.run()
  }

  const runner = _effect.run.bind(_effect)
  runner.effect = _effect
  return runner
}

export function stop(runner) {
  runner.effect.stop()
}

// ---------------------------------------------------------------------------
//  track — вызывается при ЧТЕНИИ реактивного свойства.
//  «Если сейчас есть активный эффект — свяжи его с этим (target, key)».
// ---------------------------------------------------------------------------
export function track(target, key) {
  // Никто не слушает — отслеживать нечего.
  if (!activeEffect) return

  // Достаём (или создаём) карту зависимостей для этого объекта.
  let depsMap = targetMap.get(target)
  if (!depsMap) {
    targetMap.set(target, (depsMap = new Map()))
  }

  // Достаём (или создаём) набор эффектов для конкретного свойства.
  let dep = depsMap.get(key)
  if (!dep) {
    depsMap.set(key, (dep = new Set()))
  }

  trackEffects(dep)
}

// Вынесено отдельно, потому что ref и computed хранят свой dep напрямую
// (не в targetMap) и переиспользуют эту функцию.
export function trackEffects(dep) {
  if (!activeEffect) return
  // Двусторонняя связь:
  //   dep знает про эффект (чтобы его перезапустить при изменении),
  //   эффект знает про dep (чтобы вычистить себя при cleanup).
  dep.add(activeEffect)
  activeEffect.deps.push(dep)
}

// ---------------------------------------------------------------------------
//  trigger — вызывается при ЗАПИСИ реактивного свойства.
//  «Найди все эффекты, которые читали (target, key), и перезапусти их».
// ---------------------------------------------------------------------------
export function trigger(target, key) {
  const depsMap = targetMap.get(target)
  if (!depsMap) return // этот объект никто ни разу не читал внутри эффекта

  const dep = depsMap.get(key)
  if (!dep) return

  triggerEffects(dep)
}

export function triggerEffects(dep) {
  // Важная тонкость: копируем набор в новый массив ПЕРЕД перебором.
  // Во время run() эффект вызовет cleanup (удалит себя из dep) и снова
  // добавится (track). Модифицировать Set, по которому идёт итерация, —
  // источник бесконечных циклов и пропусков. Копия защищает от этого.
  const effects = [...dep]
  for (const effect of effects) {
    // Не даём эффекту перезапустить самого себя (например, i++ внутри эффекта,
    // который читает i) — иначе бесконечная рекурсия.
    if (effect === activeEffect) continue

    if (effect.scheduler) {
      // Есть планировщик — отдаём решение ему (computed/watch/очередь UI).
      effect.scheduler()
    } else {
      effect.run()
    }
  }
}
